package com.booksystem.entity;

public class BookType {
    private int type_id;
    private String book_typename;
    private String book_typedesc;
	public int getType_id() {
		return type_id;
	}
	public void setType_id(int type_id) {
		this.type_id = type_id;
	}
	public String getBook_typename() {
		return book_typename;
	}
	public void setBook_typename(String book_typename) {
		this.book_typename = book_typename;
	}
	public String getBook_typedesc() {
		return book_typedesc;
	}
	public void setBook_typedesc(String book_typedesc) {
		this.book_typedesc = book_typedesc;
	}
    
    
}
